package _2024_08_28.chap02;

public class _01_Operator {
    public static void main(String[] args) {
        System.out.println(5 / 2); // 2
        System.out.println(2 / 4); // 0
        System.out.println(5 % 2); // 1

        //복합 대입연산자
        int num = 10;
        num += 2; // num = num + 2;
        num -= 2; // num = num - 2;
        num *= 2; // num = num * 2;
        num /= 2; // num = num / 2;
        num %= 2; // num = num % 2;

        //삼항 연산자
        //결과 = (조건) ? (참일경우) : (거짓일경우)
    }
}
